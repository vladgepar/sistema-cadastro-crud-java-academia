    public void preencheTabela() {

        //Criar uma Variável "visitanteDAO"
        //Pegar Dados dos Visitantes da "listaVisit" e Colocá-los dentro da "tabelaVisitante"
        VisitanteDAO visitDAO = new VisitanteDAO();
        String nome = pesquisarPorNome.getText();
        List<Visitante> listaVisit = visitDAO.getVisitante(nome);

        DefaultTableModel tabelaVisitante = (DefaultTableModel) tabela.getModel();

        //Comando para Fazer a "Ordenação" das "Linhas/Listas" na Tabela
        tabela.setRowSorter(new TableRowSorter(tabelaVisitante));

        //Comando "Evento CaretUpdate" para Filtrar "Linhas/Listas" na Tabela, enquanto Pesquisa por Nome
        tabelaVisitante.setNumRows(0);

        //Percorrer a "listaVisit" e Inserir na "tabelaVisitante"
        for (Visitante visit : listaVisit) {
            Object[] obj = new Object[]{
                visit.getId_visitante(), //Posição 1 do Array
                visit.getNome(), //Posição 2 do Array
                visit.getCpf(), //Posição 3 do Array
                visit.getEndereco(), //Posição 4 do Array
                visit.getTelefone(), //Posição 5 do Array
                visit.getIdade(), //Posição 6 do Array
                visit.getDiariaValor(), //Posição 7 do Array                
            };
            //Comando para Preencher as "Linhas/Listas" na Tabela Novamente
            tabelaVisitante.addRow(obj);
        }
    }

